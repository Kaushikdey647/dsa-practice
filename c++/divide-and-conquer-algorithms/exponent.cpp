template<class T>
void swap(T*a, T*b){
T temp = *b;
*b = *a;
*a = temp;
}

unsigned long long int exponent(int a, int n){
    if(a == 0)
        return 0;
    if(n == 0)
        return 1;
    if(n == 1)
        return a;
    return exponent(a, n / 2) * exponent(a, n / 2) * exponent(a, n % 2);
}

